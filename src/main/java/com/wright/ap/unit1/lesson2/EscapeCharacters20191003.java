package com.wright.ap.unit1.lesson2;

public class EscapeCharacters20191003 {
    public static void main(String[] args) {
        System.out.println("\tOrder Vlone");
        System.out.println("\t\tOrder \bVlone");
        System.out.println("Order \n\nVlone\n");
        System.out.println("Order \rVlone");
        System.out.println("\f\f\f\f\fOrder \f\f\f\f\fVlone\f\f\f\f");
        System.out.println("\'Order\' Vlone");
        System.out.println("\"Order\" Vlone");
        System.out.println("\t\t\t\tOrder \b\b\b\bVlone");
    }
}
