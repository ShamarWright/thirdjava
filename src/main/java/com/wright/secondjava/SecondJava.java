package com.wright.secondjava;

public class SecondJava {
    public static void main(String[] args) {
        System.out.println("                           Getting Tupac Money Twice over                          ");
        System.out.println("\tGetting Tupac Money Twice over");
        System.out.println("\t\tGetting Tupac Money Twice over");
        System.out.println("\t\t\tGetting Tupac Money Twice over");
        System.out.println("\t\t\t\tGetting Tupac Money Twice over");
        System.out.println("\t\t\t\t\tGetting Tupac Money Twice over");
        System.out.println("\tGetting\nTupac\nMoney\nTwice\nover");
        System.out.println("\tGetting\n Tupac\n Money\n Twice\n over");
    }
}